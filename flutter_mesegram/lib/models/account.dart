class Account {
  final String username;
  final String fullname;
  final String userImage;
  final String email;
  final String token;
  final bool isAdmin;
  final String date;
  final String posts;
  final String follow;
  final String followers;

  Account({
    this.username,
    this.fullname,
    this.userImage,
    this.email,
    this.token,
    this.isAdmin,
    this.date,
    this.follow,
    this.followers,
    this.posts,
  });
}
